public class JfInfo {
    private String xmmc;
    private String xmid;
    private String sfbx;
    private String jfje;
    private String xnxq;


    public String getXmmc() {
        return xmmc;
    }

    public void setXmmc(String xmmc) {
        this.xmmc = xmmc;
    }

    public String getXmid() {
        return xmid;
    }

    public void setXmid(String xmid) {
        this.xmid = xmid;
    }

    public String getSfbx() {
        return sfbx;
    }

    public void setSfbx(String sfbx) {
        this.sfbx = sfbx;
    }

    public String getJfje() {
        return jfje;
    }

    public void setJfje(String jfje) {
        this.jfje = jfje;
    }


    public String getXnxq() {
        return xnxq;
    }

    public void setXnxq(String xnxq) {
        this.xnxq = xnxq;
    }

    @Override
    public String toString() {
        return "JfInfo{" +
                "xmmc='" + xmmc + '\'' +
                ", xmid='" + xmid + '\'' +
                ", sfbx='" + sfbx + '\'' +
                ", jfje='" + jfje + '\'' +
                '}';
    }
}
